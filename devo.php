		<h2>1. Pide la devolución del producto</h2>
		Para devolver un producto, busca la opción “Devolver o cambiar gratis” que aparece en el menú de la compra que ya no quieres y sigue los pasos. Te daremos una etiqueta o código de devolución gratis para que envíes el producto de vuelta. Según la información que tengamos sobre la devolución, a veces te pediremos que hables con el vendedor antes de tener la etiqueta o el código.

		También tendrás que hablar con el vendedor si al comprar acordaste la entrega con él. Podrás hacerlo a través de la mensajería o iniciando un reclamo desde la compra, si no se pueden poner de acuerdo. Ten en cuenta que en esos casos no te vamos a dar una etiqueta o código de devolución pero te ayudaremos a que envíes de vuelta tu compra.

		<h2>2. Prepara el paquete para enviarlo</h2>
		Antes de preparar el paquete, revisa que el producto esté en las mismas condiciones que lo recibiste, sin usar y con todos sus accesorios y etiquetas.
		Guarda el producto en su envoltorio original o en uno alternativo si no lo tienes. 
		Embala el producto. Si es frágil, utiliza un embalaje seguro que lo proteja. 
		Imprime la etiqueta y pégala o escribe el código de devolución en el paquete.
		Entrega el paquete al correo o agencia de Mercado Libre que te asignemos.
		Si compraste más de una unidad del mismo producto, debes devolver todos los artículos para recuperar tu dinero.

		<h2>3. Recibe el reembolso del dinero</h2>
		Haremos el reembolso 3 días hábiles después de que llegue el producto, una vez que revisemos si cumple con las políticas de devolución. Sin embargo, algunas veces podemos hacer el reembolso ni bien entregas el producto, para que tengas el dinero más rápido.
		Una vez que hagamos el reembolso, podrás ver todos los detalles sobre el monto, lugar y día de acreditación del dinero desde el estado de tu compra. 
		Para más información puedes consultar los tiempos de acreditación de cada medio de pago.

		<h2>¿Cuánto tiempo tengo para enviar el producto?</h2>
		La etiqueta o el código que te damos tiene validez por 5 días. Si por algún motivo no puedes enviar el paquete durante ese período, podrás pedirnos otra etiqueta o código siempre que no hayan pasado más de 30 días desde que recibiste tu compra. Para hacerlo, tendrás que volver a iniciar la devolución con la opción “Devolver o cambiar gratis”.

		<h2>¿Qué pasa si me arrepiento de devolver?</h2>
		Si lo deseas, puedes cancelar la devolución. Y si más adelante quieres hacer la devolución, podrás volver a comenzar, siempre que no hayan pasado los 30 días que tienes desde que recibes el producto.
		<h2>Politicas de devolucion</h2>
		Los productos pueden devolverse gratis, sin importar el motivo.
		Solo hay algunos productos específicos que no tienen devolución. En ese caso, podrás acordar una solución con el vendedor iniciando un reclamo si lo necesitas.
